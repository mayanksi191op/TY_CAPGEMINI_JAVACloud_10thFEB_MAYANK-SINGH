package com.tyss.cg.loops;

public class DoWhileLoopEx {
	public static void main(String[] args) {

		int i = 1;
		int j = 1;

		do {
			System.out.println("vlaue of i in do: " + i);
			i++;
		} while (i <= 10);

		do {
			System.out.println("value of j is: " + j);
			j++;
		} while (j < 0);
		
	
	}

}
