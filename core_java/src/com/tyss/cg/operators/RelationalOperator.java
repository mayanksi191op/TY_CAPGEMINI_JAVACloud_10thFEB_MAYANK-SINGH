package com.tyss.cg.operators;

public class RelationalOperator {

	public static void main(String[] args) {

		int i = 97;
		int j = 45;

		System.out.println("i < j: " + (i < j));
		System.out.println("i > j: " + (i > j));
		System.out.println("i <= j: " + (i <= j));
		System.out.println("i >= j: " + (i >= j));
		System.out.println("i = j: " + (i == j));
		System.out.println("i != j: " + (i != j));

	}
}
