
public class Star5 {
	public static void main(String[] args) {
		 for(int i=0; i<4; i++)
		 {
			 for(int j=0; j<=i; j++)
			 {
				 System.out.print("x");
			 }
			 System.out.println();	 
		 }
				 
		for(int p=0; p<3; p++)	 
		{
			 for(int k=p; k<3; k++)
			 {
				 System.out.print("x");
			 }
			 System.out.println();
			 
		 }
	}

}
